
# variable = tipe data int
nomer = 1
# variable = tipe data String
nama = "arif rahman hakim"
# variable = tipe data integer
umur = 20
# variable = tipe data boolean
mahasiswa = True

# mencetak hasil tipe data serta mengetahui tipe data yang digunakan.
print("Nama saya adalah ", nama, "Type =", type(nama))
print("Umur Saya adalah ", umur, "Type =", type(umur))
print("Apakah Saya mahasiswa ? ", mahasiswa, "Type =", type(mahasiswa))

# casting tipe data / mengubah tipe data
print(int(nomer))
print(str(nama))
print(float(umur))
print(bool(mahasiswa))