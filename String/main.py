# Menggunakan \' agar petik dibaca menjadi string
data = 'sekarang hari jum\'at'
print(data)

# backslash \\
print('C:\\user\\Usopp')

# tab \t
print('ucup\totong')

# enter \n
print('test\nenter.') # LF = Line feed
print('test\renter.') # CR = Carriage Return
print('test\r\nenter.') # CRLF = Line feed carriage return

# backspace \b
print('test \bsanni')

# Raw string
print(r'C: \n\t\g\b\b') # Semuanya akan di print

print(r"""
Nama : Alfad Sabil Haq
NIM : 2021500005 \new noermal
""")